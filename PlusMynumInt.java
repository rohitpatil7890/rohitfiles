
public class PlusMynumInt {
		static int Mynum1(int x, int y) {
			return x+y;
		}
		static double Mynum2(double a,double b) {
			return a-b;
		}
	public static void main(String[] args) {
		int z=Mynum1(3,44);
		double c=Mynum2(12.22,10.21);
		
		System.out.println(z);
		System.out.println(c);
		
	}

}
